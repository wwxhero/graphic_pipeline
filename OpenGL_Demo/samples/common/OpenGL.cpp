//-----------------------------------------------------------------------------
void* LoadEntryPoint(const char* name) {
    void* proc = SDL_GL_GetProcAddress(name);
    printf("%-40s %s\n", name, (proc ? "supported" : "not supported"));
    if (!proc) {
        ostringstream ss;
        ss << "OpenGL " << MajorVer << "." << MinorVer <<
            " is not supported by the graphics driver (failed to load " << name << " function).";
        throw runtime_error(ss.str());
    }
    return proc;
}
